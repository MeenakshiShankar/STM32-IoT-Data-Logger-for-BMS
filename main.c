/**
  * @brief  Function implementing the Sender1 thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartSender1 */
void StartI2CTask(void *argument)
{
  HAL_StatusTypeDef ret;
  uint8_t buf_tx[2], buf_rx[2];
  uint16_t val_volt, val_curr;
  
  while(1)
  {
     osMutexAcquire(dataMutexHandle, osWaitForever);
    // Collect bus voltage
    buf_tx[0] = INA226_BUS_VOLTAGE;
    ret = HAL_I2C_Master_Transmit(&hi2c1, INA226_ADRS, buf_tx, 1, HAL_MAX_DELAY);
    if (ret == HAL_OK)
    {
      HAL_I2C_Master_Receive(&hi2c1, INA226_ADRS, buf_rx, 2, HAL_MAX_DELAY);
      val_volt = (buf_rx[0] << 8) | buf_rx[1];
      bus_volt = (val_volt / 100) * 2;
    }

    // Collect current
    buf_tx[0] = INA226_CURRENT;
    ret = HAL_I2C_Master_Transmit(&hi2c1, INA226_ADRS, buf_tx, 1, HAL_MAX_DELAY);
    if (ret == HAL_OK)
    {
      HAL_I2C_Master_Receive(&hi2c1, INA226_ADRS, buf_rx, 2, HAL_MAX_DELAY);
      val_curr = (buf_rx[0] << 8) | buf_rx[1];
      bus_curr = (val_curr * bus_volt) / 10000;
    }

    // Calculate state of charge (SOC)
    if (bus_volt > 0)
      soc = (bus_volt / 11);
    else
      soc = 100;

    // Lock mutex and update shared data
   
    sprintf(buf, "%ldV %ldmA %dp \r\n", bus_volt, bus_curr, soc);
    osMutexRelease(dataMutexHandle);

    // Sleep for a while before the next collection
    osDelay(1000);
  }
}


