 /* USER CODE END Header_StartReceiver */

void StartTransmitTask(void *argument)
{
  while(1)
  {
    // Lock mutex to access shared data
    // Send data to ThingSpeak via ESP8266
    osMutexAcquire(dataMutexHandle, osWaitForever);
    HAL_UART_Transmit(&huart2, (uint8_t *)buf, strlen(buf), HAL_MAX_DELAY);
    osMutexRelease(dataMutexHandle);

    // Format data for ThingSpeak


    HAL_UART_Transmit(&huart1, (uint8_t *)buf, strlen(buf), HAL_MAX_DELAY);

    // Delay to prevent sending too many requests (ThingSpeak rate limit)
    osDelay(15000);  // 15 seconds
  }
}


